	ImageProducer[] ips;
	int items=346,animals=6;
	Toolkit t;

	public ItemImgSep() {}
	public ItemImgSep(Object c, Image a, Toolkit tt) {
		t = tt;
		ips = new ImageProducer[2];
		if (a != null)
			ips[0] = a.getSource();

		if (c instanceof DSMapCanvas)
			doGame((DSMapCanvas)c);
		else if (c instanceof DSEMapCanvas)
			doEditor((DSEMapCanvas)c);
	}
	public void doEditor(DSEMapCanvas map) {
		map.items = new Image[items];
		map.nitems = items;
		map.itemcoords = itemcoords;
		map.itall = itall;
	}
	public void doGame(DSMapCanvas map) {
		map.items = new Image[items+(animals*8)];
		map.nitems = items;
		map.itemcoords = itemcoords;
		map.itall = itall;
		seperateAnimals(map.items);
		//map.spells = seperateSpells();
	}
	public void seperateAnimals(Image[] retval) {

		//bunny
		for (int i = 0; i < 8; i++) {
			retval[items+i] = doCrop(0,(i+1)+(40*i),1,40,24);
		}
		//snail
		for (int i = 0; i < 8; i++) {
			retval[items+i+8] = doCrop(0,(i+1)+(37*i),26,37,19);
		}
		//lizard
		for (int i = 0; i < 8; i++) {
			retval[items+i+16] = doCrop(0,(i+1)+(40*i),46,40,19);
		}
		//fish
		for (int i = 0; i < 8; i++) {
			retval[items+i+24] = doCrop(0,(i+1)+(37*i),66,37,18);
		}
		//squirrel
		for (int i = 0; i < 8; i++) {
			retval[items+i+32] = doCrop(0,(i+1)+(38*i),85,38,19);
		}
		//crow
		for (int i = 0; i < 8; i++) {
			retval[items+i+40] = doCrop(0,(i+1)+(36*i),105,36,18);
		}
	}
